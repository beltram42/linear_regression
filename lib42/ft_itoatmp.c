#include "lib42.h"

char	*ft_itoa(int i)
{
	static char	buf[9 + 2];
	char		*p;

	ft_bzero(buf, sizeof(char) * 11);
	p = buf + 9 + 1;
	if (i >= 0)
	{
		while (i != 0)
		{
			*--p = '0' + (i % 10);
			i /= 10;
		}
		return (p);
	}
	else
	{
		while (i != 0)
		{
			*--p = '0' - (i % 10);
			i /= 10;
		}
		*--p = '-';
	}
	return (p);
}
